# there are 2 ways of traverse in python
# itemwise or stepwise

# =========== item wise ===========
l = [1,2,34,45,55]
for i in l :
    print(i)    

# =========== stepwise ===========
l1 = [1,2,3,4,5,6]
for i in range (0,len(l1)):
    # print(i) agar ese karenge to 0 se start karke print karega agar isko list me store karke karenge to 1 se dega 
    print(l1[i])

